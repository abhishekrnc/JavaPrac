package com.HelloWorld;

public class operators {

	public static void main(String[] args) {
		
		System.out.println(9/5);
		System.out.println(9.0/5);
		
		System.out.println(9 + 5);
		System.out.println(9 - 5);
		System.out.println(9 / 5);
		System.out.println(9 * 5);
		
		System.out.println(9 % 5);
		
		System.out.println('a' + 'b');
	}

}
